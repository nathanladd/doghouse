// Set active navigation based on current page
function setActiveNavigation() {
  const currentPath = window.location.pathname;
  const navLinks = document.querySelectorAll('.sidebar a');
  
  navLinks.forEach(link => {
    if (link.getAttribute('href') === currentPath || 
        link.getAttribute('href') === window.location.pathname.split('/').pop()) {
      link.classList.add('active');
    }
  });
}

function buildHeader() {
  const header = document.querySelector('.site-header');
  if (!header) return;

  const breadcrumbData = header.dataset.breadcrumb || '';
  const badge = header.dataset.badge || '';

  const parts = window.location.pathname.replace(/\\/g, '/').split('/').filter(Boolean);
  const acIdx = parts.lastIndexOf('bobcat-ac');
  const depth = acIdx === -1 ? 1 : parts.length - acIdx - 1;
  const courseHref = '../'.repeat(depth - 1) + 'index.html';
  const doghouseHref = '../'.repeat(depth) + 'index.html';

  const breadcrumbHtml = breadcrumbData
    ? `Service Training <b>›</b> ${breadcrumbData}`
    : 'Service Training';

  header.innerHTML = `
    <div class="logo"><a href="${courseHref}" style="text-decoration: none; color: inherit;">Bobcat AC: <span>Making Stuff Less Hot</span></a></div>
    <div class="breadcrumb">${breadcrumbHtml}</div>
    <div class="header-search" role="search">
      <input type="search" class="header-search-input" placeholder="Search this course&hellip;" aria-label="Search the Bobcat AC course" autocomplete="off" spellcheck="false">
      <div class="header-search-results" role="listbox" aria-label="Search results"></div>
    </div>
    <button type="button" class="tv-mode-toggle" onclick="toggleTvMode()" title="Enlarge text for viewing on a TV from a distance">&#128250; Large Text</button>
    <a href="${doghouseHref}" class="home-link">&#8592; Rudi-HQ</a>
    ${badge ? `<div class="header-badge">${badge}</div>` : ''}
  `;

  const toggleBtn = header.querySelector('.tv-mode-toggle');
  if (toggleBtn) {
    toggleBtn.setAttribute('aria-pressed', document.documentElement.classList.contains('tv-mode') ? 'true' : 'false');
  }

  // Resolve the index path the same way the logo/home links are resolved:
  // "depth - 1" is the number of "../" hops from this page up to bobcat-ac/.
  initSearch('../'.repeat(Math.max(depth - 1, 0)));
}

// ---------------------------------------------------------------------------
// Site search (bobcat-ac subsite only)
//
// Loads a prebuilt index once, on first use, then matches entirely in the
// browser — no server, no build step at runtime. The index is generated by
// tools/build-search-index.ps1 into assets/search-index.json; re-run that
// script whenever course content changes so results stay current.
//
// NOTE: the index is fetched, so the site must be served over http(s)
// (the Rudi server, GitHub Pages, or a local `npx serve`). Opening pages
// directly as file:// blocks fetch() for security — search degrades to a
// clear "unavailable" message rather than failing silently.
//
// Each page entry carries a sections[] array ({id, title, text}) built by
// the same script from the same .section-title headings that
// assignSectionIds() above turns into real DOM ids — see that comment for
// how the two stay in sync. runSearch() uses sections[] both to rank
// heading/body matches and to pick which single section a result should
// deep-link into via #id.
// ---------------------------------------------------------------------------
var searchIndex = null;          // [{url, title, breadcrumb, sections, text}]
var searchIndexState = 'idle';   // idle | loading | ready | error
var searchBase = '';             // "../" hops from the current page to bobcat-ac/
var searchSelected = -1;         // keyboard-highlighted result index

function initSearch(base) {
  searchBase = base;
  const input = document.querySelector('.header-search-input');
  const results = document.querySelector('.header-search-results');
  if (!input || !results) return;

  input.addEventListener('focus', loadSearchIndex);
  input.addEventListener('input', function () {
    loadSearchIndex().then(function () { runSearch(input.value); });
  });
  input.addEventListener('keydown', onSearchKeydown);

  // Any click outside the search widget closes the results dropdown.
  document.addEventListener('click', function (e) {
    if (!e.target.closest('.header-search')) closeSearchResults();
  });
}

function loadSearchIndex() {
  if (searchIndexState === 'ready' || searchIndexState === 'loading') {
    return Promise.resolve();
  }
  searchIndexState = 'loading';
  return fetch(searchBase + 'assets/search-index.json')
    .then(function (r) { if (!r.ok) throw new Error(r.status); return r.json(); })
    .then(function (data) { searchIndex = data; searchIndexState = 'ready'; })
    .catch(function () {
      searchIndexState = 'error';
      const results = document.querySelector('.header-search-results');
      if (results) {
        results.innerHTML = '<div class="hs-note">Search is unavailable. Serve the site over http (not file://) and rebuild the index.</div>';
        results.classList.add('open');
      }
    });
}

function runSearch(rawQuery) {
  const results = document.querySelector('.header-search-results');
  if (!results) return;
  const query = (rawQuery || '').trim().toLowerCase();
  searchSelected = -1;

  if (query.length < 2 || searchIndexState !== 'ready') {
    closeSearchResults();
    return;
  }

  const terms = query.split(/\s+/);
  const matches = [];
  searchIndex.forEach(function (page) {
    const sections = page.sections || [];
    const title = (page.title || '').toLowerCase();
    const headings = sections.map(function (s) { return s.title || ''; }).join(' ').toLowerCase();
    const text = (page.text || '').toLowerCase();
    let score = 0;
    let matchedAll = true;
    // AND semantics: every term must appear somewhere. Title hits outrank
    // heading hits, which outrank body hits.
    for (let i = 0; i < terms.length; i++) {
      const term = terms[i];
      let s = 0;
      if (title.includes(term)) s += 10;
      if (headings.includes(term)) s += 4;
      if (text.includes(term)) s += 1;
      if (s === 0) { matchedAll = false; break; }
      score += s;
    }
    if (matchedAll) {
      matches.push({ page: page, score: score, section: pickBestSection(sections, terms) });
    }
  });
  matches.sort(function (a, b) { return b.score - a.score; });
  renderSearchResults(matches.slice(0, 8), terms);
}

// Which single section within a matching page should the result deep-link
// to? Independent of the whole-page score above — a page can win on title
// match alone while one particular section holds the actual term hits.
function pickBestSection(sections, terms) {
  let best = null;
  let bestScore = 0;
  sections.forEach(function (sec) {
    const title = (sec.title || '').toLowerCase();
    const body = (sec.text || '').toLowerCase();
    let s = 0;
    terms.forEach(function (term) {
      if (title.includes(term)) s += 4;
      if (body.includes(term)) s += 1;
    });
    if (s > bestScore) { bestScore = s; best = sec; }
  });
  return bestScore > 0 ? best : null;
}

function renderSearchResults(matches, terms) {
  const results = document.querySelector('.header-search-results');
  if (!results) return;

  if (matches.length === 0) {
    results.innerHTML = '<div class="hs-note">No matches.</div>';
    results.classList.add('open');
    return;
  }

  results.innerHTML = matches.map(function (m, i) {
    const page = m.page;
    const section = m.section;
    // Deep-link into the matched section when we found one; otherwise the
    // result just opens the page (assignSectionIds() put the #id there).
    // ?hl=<terms> tells the destination page which words to highlight in
    // its own content — see applySearchHighlight(), run on that page's load.
    const hl = '?hl=' + encodeURIComponent(terms.join(' '));
    const href = searchBase + page.url + hl + (section ? '#' + section.id : '');
    const crumbParts = [];
    if (page.breadcrumb) crumbParts.push(escapeHtml(page.breadcrumb));
    if (section) crumbParts.push(escapeHtml(section.title));
    const crumb = crumbParts.length
      ? '<div class="hs-crumb">' + crumbParts.join(' <b>&rsaquo;</b> ') + '</div>'
      : '';
    const snippetSource = section ? (section.text || '') : (page.text || '');
    return '<a class="hs-item" role="option" href="' + escapeHtml(href) + '" data-index="' + i + '">'
      + '<div class="hs-title">' + highlight(page.title || page.url, terms) + '</div>'
      + crumb
      + '<div class="hs-snippet">' + buildSnippet(snippetSource, terms) + '</div>'
      + '</a>';
  }).join('');
  results.classList.add('open');
}

// A short excerpt around the first matched term, with terms highlighted.
function buildSnippet(text, terms) {
  const lower = text.toLowerCase();
  let at = -1;
  terms.forEach(function (t) {
    const i = lower.indexOf(t);
    if (i !== -1 && (at === -1 || i < at)) at = i;
  });
  const start = at === -1 ? 0 : Math.max(0, at - 40);
  let slice = text.slice(start, start + 160);
  if (start > 0) slice = '…' + slice;
  if (start + 160 < text.length) slice = slice + '…';
  return highlight(slice, terms);
}

function highlight(text, terms, markClass) {
  let out = escapeHtml(text);
  const openTag = markClass ? '<mark class="' + markClass + '">' : '<mark>';
  terms.forEach(function (t) {
    if (!t) return;
    out = out.replace(new RegExp('(' + escapeRegExp(t) + ')', 'ig'), openTag + '$1</mark>');
  });
  return out;
}

function escapeHtml(s) {
  return String(s).replace(/[&<>"']/g, function (c) {
    return { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c];
  });
}

function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function onSearchKeydown(e) {
  const results = document.querySelector('.header-search-results');
  if (!results) return;
  const items = results.querySelectorAll('.hs-item');

  if (e.key === 'Escape') { closeSearchResults(); e.target.blur(); return; }
  if (!items.length) return;

  if (e.key === 'ArrowDown') {
    e.preventDefault();
    searchSelected = Math.min(searchSelected + 1, items.length - 1);
    updateSearchSelection(items);
  } else if (e.key === 'ArrowUp') {
    e.preventDefault();
    searchSelected = Math.max(searchSelected - 1, 0);
    updateSearchSelection(items);
  } else if (e.key === 'Enter') {
    const target = searchSelected >= 0 ? items[searchSelected] : items[0];
    if (target) { e.preventDefault(); window.location.href = target.getAttribute('href'); }
  }
}

function updateSearchSelection(items) {
  items.forEach(function (it, i) { it.classList.toggle('hs-active', i === searchSelected); });
  if (searchSelected >= 0) items[searchSelected].scrollIntoView({ block: 'nearest' });
}

function closeSearchResults() {
  const results = document.querySelector('.header-search-results');
  if (results) results.classList.remove('open');
  searchSelected = -1;
}

// ---------------------------------------------------------------------------
// Section anchors (for search deep-linking)
//
// .section-title headings have no id in the HTML. Rather than hand-editing
// ~100 headings across the site, ids are computed from the heading text at
// load time here, AND independently from the same heading text by
// tools/build-search-index.ps1 when it builds the index. Both sides run the
// identical slug rule against the identical input, so they land on the same
// id without ever communicating: assignSectionIds() -> "refrigerant-hazards"
// on this page's <div>, Get-Slug in the .ps1 -> "refrigerant-hazards" in
// search-index.json's sections[].id. If you change this rule, change
// Get-Slug in build-search-index.ps1 to match, or every existing deep link
// in the index silently stops landing on its target.
// ---------------------------------------------------------------------------
function slugify(text) {
  return String(text)
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function assignSectionIds() {
  const counts = {};
  document.querySelectorAll('.section-title').forEach(function (el) {
    if (el.id) return; // don't clobber an id someone set by hand
    let slug = slugify(el.textContent) || 'section';
    counts[slug] = (counts[slug] || 0) + 1;
    // First occurrence of a slug on the page keeps it bare; repeats of the
    // same heading text get -2, -3, ... in document order (must match the
    // dedup order in Get-Slug's caller in the .ps1, since both walk the
    // page top-to-bottom the same way).
    if (counts[slug] > 1) slug = slug + '-' + counts[slug];
    el.id = slug;
  });
}

// Ids above are assigned after the browser parses the page, so on a fresh
// navigation to page.html#some-slug the browser's own automatic
// fragment-scroll can lose the race (the id didn't exist yet when it looked).
// Scroll to it explicitly once we know the id is there.
function scrollToHashIfPresent() {
  if (!window.location.hash) return;
  const target = document.getElementById(window.location.hash.slice(1));
  if (target) target.scrollIntoView();
}

// ---------------------------------------------------------------------------
// Highlighting the matched text on the destination page
//
// Landing on the right section isn't quite enough to see AT A GLANCE what
// you searched for, so search results also pass the query terms through as
// ?hl=<terms>. On load, this wraps every matching word on the page in
// <mark class="search-hit"> and scrolls straight to the first one.
//
// This is scoped to the ONE matched section, not the whole page: a term
// like "recovery" appears in a dozen sections across the course, and
// naively flashing/scrolling to whichever occurrence comes first in the
// document would frequently land you in the WRONG section — silently
// undoing the section deep-link that pickBestSection() in main.js's search
// already got right. Sections in this site are flat sibling <div>s under
// .main (see build-search-index.ps1's section-slicing comment for the same
// assumption on the indexing side), so "this section's content" is just
// "the heading plus its siblings up to the next .section-title".
// ---------------------------------------------------------------------------
function getHighlightTermsFromUrl() {
  const raw = new URLSearchParams(window.location.search).get('hl');
  if (!raw) return [];
  return raw.split(/\s+/).map(function (t) { return t.trim(); }).filter(Boolean);
}

function getSectionSiblings(headingEl) {
  const nodes = [headingEl];
  let el = headingEl.nextElementSibling;
  while (el && !el.classList.contains('section-title')) {
    nodes.push(el);
    el = el.nextElementSibling;
  }
  return nodes;
}

function collectTextNodes(root) {
  const out = [];
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode: function (n) {
      const p = n.parentElement;
      if (!p || p.closest('script, style')) return NodeFilter.FILTER_REJECT;
      if (!n.nodeValue || !n.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  let n;
  while ((n = walker.nextNode())) out.push(n);
  return out;
}

// Wraps every match with <mark class="search-hit"> inside the given
// elements (searched in order), marks the very first hit with an extra
// "search-hit-primary" class (for the flash animation / scroll target),
// and returns that first mark element (or null if nothing matched).
function highlightWithinNodes(elements, terms) {
  const testRes = terms.map(function (t) { return new RegExp(escapeRegExp(t), 'i'); });
  let first = null;
  elements.forEach(function (container) {
    collectTextNodes(container).forEach(function (textNode) {
      if (!testRes.some(function (re) { return re.test(textNode.nodeValue); })) return;
      const html = highlight(textNode.nodeValue, terms, 'search-hit');
      const frag = document.createRange().createContextualFragment(html);
      if (!first) first = frag.querySelector('mark.search-hit');
      textNode.replaceWith(frag);
    });
  });
  if (first) first.classList.add('search-hit-primary');
  return first;
}

function applySearchHighlight() {
  const terms = getHighlightTermsFromUrl();
  if (!terms.length) return null;

  let scope = null;
  if (window.location.hash) {
    const heading = document.getElementById(window.location.hash.slice(1));
    if (heading && heading.classList.contains('section-title')) {
      scope = getSectionSiblings(heading);
    }
  }
  // No section anchor (a title-only match with no section hit) - fall back
  // to the whole page rather than skip highlighting entirely.
  if (!scope) scope = [document.querySelector('.main') || document.body];

  return highlightWithinNodes(scope, terms);
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
  assignSectionIds();
  // If we arrived from a search result, scroll to the actual matched text;
  // otherwise (a plain link/bookmark to #section) just scroll to the section.
  const firstHit = applySearchHighlight();
  if (firstHit) {
    firstHit.scrollIntoView({ block: 'center' });
  } else {
    scrollToHashIfPresent();
  }
  buildHeader();
  setActiveNavigation();
  
  // Add click handlers to all navigation links
  document.querySelectorAll('.sidebar a').forEach(link => {
    link.addEventListener('click', function(e) {
      // Remove active class from all links
      document.querySelectorAll('.sidebar a').forEach(l => l.classList.remove('active'));
      // Add active class to clicked link
      this.classList.add('active');
    });
  });
  
  // Initialize all checkboxes
  document.querySelectorAll('input[type="checkbox"]').forEach(checkbox => {
    checkbox.addEventListener('change', function() {
      toggleCheck(this);
    });
  });
});

